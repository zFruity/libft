#include "libft.h"

void	*ft_bzero(void *str, size_t n)
{
	size_t	i;
	char	*ptr;

	ptr = str;
	i = 0;
	while (i < n)
	{
		ptr[i] = 0;
		i++;
	}
	return (str);
}
